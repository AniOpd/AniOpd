public class ch_4_opreator {
    public static void main(String[] arg){
        // assignment opretor
        int b=9;
        b+=6;
        System.out.println(b);
        //logical opreator
        System.out.println(89>88 && 89<78);
        System.out.println(89<90 || 89>78);
        //bitwise opreator
        System.out.println(2&3);
    }
}
